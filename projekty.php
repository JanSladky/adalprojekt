                        </p>

                        <h3>Tady se necítím bezpečně.</h3>
                        <p class="justify">Hnědé puntíky mnohdy doprovází ty červené - tam, kde se lidé necítí bezpečně by si logicky přáli změnu. V diskuzi zazněly nejen výtky k nekonvenčnímu chování různých sociálních skupin obyvatel města, diskuze se také zaměřila na dopravně nebezpečná místa (včetně situace před školami v době ranní a odpolední špičky). Tuto barvu můžeme tedy vidět na Kruhovém náměstí, v ulici Pod Střelnicí, nebo na Plzeňské směrem na Most. Vyrovnaný počet má také svah pod pivovarem - pro svůj hustý porost jsou tato místa velmi nepřehledná a tudíž i pocitově nebezpečná (podobně jako Havlíčkovo náměstí)</p>

                        <h3>Tady vidím příležitost pro změnu. .</h3>
                        <p class="justify">Puntíky potenciálu změny se nešetřilo. Obě vlaková nádraží, Dreherův pivovar / Fruta, městský park, Havlíčkovo náměstí, skatepark, ale i náměstí Svobody. To jsou místa, která by se podle lidí měla zlepšit. Jiná místa vychází z nedávných událostí, či sezónní zkušenosti - křižovatka na Plzeňské, městské koupaliště, nebo třeba letní kino, o kterém se na podzim dost mluvilo ve vztahu k Lüdersdorfově vile. </p>

