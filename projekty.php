            <dd><p class="text-left">KDE: po žateckých sídlíštích<br>
            KDY: 30. března 2019</p>
            <p class="justify">Na konci března se uskutečnila městská vyjížďka na kolech. Navázali jsme na úspěšný model komentované procházky uspořádané o Den architektury v říjnu 2018, avšak vyměnili jsme pěškobus za bicykl. Účastníci si společně s tradičním duem Evou a Monikou projeli pětikilometrovou trasu od hřbitova přes sídliště Jih až do Podměstí a připomněli si historický vývoj a sociálně - společenský kontext doby.
                Proč sídliště? Na sídlišti žije v současné době třetina obyvatel ČR. Myslíme si, že je jim věnovaná nedostatečná pozornost, stejně jako panelákům samotným. Cyklovyjížďka navázala na naše aktivity, které od začátku směřujeme spíše mimo historické centrum a do trochu opomíjeného období naší historie.
