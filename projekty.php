                      <h2>Co mapa ukázala?</h2>
                      <h3>Tady jsem na město hrdý.</h3>
                      <p class="justify">Pozitivní odezvu mají již zrekonstruované části města v okolí Chrámu chmele a piva, klášterní zahrada, nebo stadion Mládí. Často se puntíky hrdosti objevují také kolem základních a středních škol, které byly vždy vzorovými veřejnými stavbami (realizovanými často na základě architektonických soutěží). Puntíky hrdosti se často objevují společně s těmi pro trávení volného času (procházkový okruh kolem řeky. místa zajímavých výhledů na město). To ale neplatí pro ty umístěné v centru města - hrdost spojenou s centrem, architekturou a historií už nedoprovází zelený pobytový puntík. </p>

                      <h3>Tady trávím volný čas.</h3>
                      <p class="justify">
