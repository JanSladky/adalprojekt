                    <h2>Co to je?</h2>
                        <p class="justify">MAPA POCITŮ je nástroj tzv. geoparticipace. Barevnými lepíky může větší množství lidí vyjádřit svůj názor do mapy města. Právě mapový podklad je pro svou jednoduchost a názornost výborným můstkem mezi veřejností a městem, úřadem, mladými, starými, chytrými i méně chytrými (na jejichž názoru však nezáleží o nic méně). </p>

                    <h2>Jak to funguje?</h2>
                    <p class="justify">Místa s největším počtem barevných označení identifikujeme jako ta, která se s daným pocitem shodují nejvíce - lidé se na nich objektivně shodnou. Pokud mapu vyplní dostatek lidí, lze ji považovat za kvantitativní (množstevní) průzkum. Ta přímo nevysvětluje příčiny, avšak je dobrým startem pro hlubší analýzu potřeb veřejnosti. Proto také nese název “pocitová” - tvorba se řídí spíše intuicí, zkušeností, vzpomínkou, či prostým přáním. </p>

