            <h2 class="post-header-h2">Je to transparentní? Tady je pár čísel….</h2>
            <p class="justify">V létě 2018, jsme na schůzce s městem a ateliérem CCEA MOBA diskutovali o částce 800 000,- Kč za organizaci soutěže. Oficiálně vypsaná soutěž je však dražší (jen ceny za první tři místa jsou stanoveny na 600 000,-, místo původních 350 000,-). Důvodem může být kromě větší rozpracovanosti zadání také změna Soutěžního řádu, která počítá kromě předpokládaných realizačních nákladů také s náročností samotného zadání (inu, je rozdíl navrhovat školku, letiště, nebo náměstí). S každým přibývajícím rokem navíc náklady stoupají.</p>

            <p class="justify">Pro účely soutěže jsou náklady na obnovu náměstí stanoveny na 42 200 000 Kč. Z toho na další zakázku (tedy prováděcí dokumentaci a autorský dozor) je počítáno s 4 200 000 Kč. Na samotnou realizaci tedy zbývá 38 000 000 Kč. To činí při ploše řešeného území, která je 14 670 m2 zhruba 2 600 Kč / m2 (potažmo m3).
                Pro srovnání: Oprava Nerudova náměstí vychází při řešené ploše 2 400 m2 a smluvní ceně 8 700 000 Kč na 3 600 Kč / m2.
            </p>
            <p class="justify">Čísla se budou během navrhování určitě ještě měnit. Území je rozsáhlé a v případě realizace bude pravděpodobně přistoupeno k etapizaci celé obnovy.</p>

            <h2 class="post-header-h2">Na cestě za obnovou, aneb co bude po soutěži?</h2>

            <p class="justify">Po vyhlášení vítězů město bude jednat s vítězem o procesu dalšího projektování. Prováděcí dokumentace sice zabere několik dalších měsíců, ale výhoda soutěže je v připravenosti podkladů: Už nyní mají soutěžící k dispozici geodetické zaměření a vedení inženýrských sítí, které poptal a shromáždil Odbor rozvoje města. Projektové práce mohou začít prakticky ihned po podepsání smlouvy.</p>

            <p class="justify">Mělo by být zorganizováno také setkání s veřejností, představení návrhů samotnými autory. Zpětná vazba pomůže autorům s dopracováním studie a zároveň budou lidé obeznámeni s dalším procesem. </p>

            <p class="justify">A dál? To už je vše na filozofii vedení města a jeho finančních možnostech. Nejprve se musí akce dostat do investičního plánu města na další rok. Návrhy těchto plánů dodává zastupitelstvu Odbor rozvoje města a hlasuje se o nich na konci každého roku. V plánu je na záměr vyčleněna částka, případně je akce rozdělena na etapy. Pokud bude trh stabilní a stavební firma vysoutěží zakázku za předpokládaný soutěžní propočet, nebo jinou odsouhlasenou cenu, práce mohou začít. Kdy to bude? Náš optimistický odhad říká v roce 2021 / 2022.</p>

