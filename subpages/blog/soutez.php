            <p class="justify">Na začátku března se na webu České komory architektů (ČKA) objevila regulérní architektonická soutěž na Havlíčkovo náměstí. Měli jsme obrovskou radost a pořád máme. Skončila totiž etapa, která započala v roce 2018 a zároveň byl splněn slib, který jsme dali nejen sobě, ale také veřejnosti. Vyhlášením soutěže ale vše teprve začíná.</p>

            <h2 class="post-header-h2">Proč soutěž? A co to znamená?</h2>
            <p class="justify">Architektonická soutěž je způsob, jak najít vhodný návrh pro investiční záměr. Jako jiné soutěže, i tato má pravidla a ti nejlepší soutěžící jsou za svůj výkon ocenění. Soutěž o návrh porovnává různé pohledy na jeden konkrétní problém a tudíž prezentuje více způsobů řešení. A v tom je právě její velká výhoda. Za o něco více peněz máte opravdu hodně moc muziky.
            </p>

            <h2 class="post-header-h2">Důležitá fakta související se soutěží:</h2>
