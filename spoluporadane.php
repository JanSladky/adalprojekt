<dt><h1 class="h1-projekty">Den architektury 2018</h1></dt>
<dd><p class="text-left">KDE: Žatec <br />KDY: 29. září 2018</p>
<p class="text-left">POŘADATEL: Kruh, DA (Den architektury), FA (Film a architektura)</p>
<p class="justify">V rámci celorepublikového festivalu Den architektury, s letošním tématem 100. výročí republiky, uspořádaly Eva a Monika procházku po žateckých domech, realizovaných v posledním století. Start výpravy byl naplánován před brutalistním kulturním domem Moskva s úvodem obhajujícím vybrané místo a také spolkovou aktivitu k této lokalitě. Během dvouhodinové procházky posluchači shlédli realizace "čtyřprocentního umění" v Podměstí, dozvěděli se podrobnosti o autorech tamních škol a dále pokračovali na Pražské předměstí. Zde se společně s průvodkyněmi podívali do interiéru budovy Známkovny a výpravu zakončili před Chrámem chmele a piva - nejvýznamnější porevoluční realizací v Žatci, oceněnou v roce 2013 Klubem za starou Prahu.</p>

