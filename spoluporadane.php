<dt><h1 class="h1-projekty">Den architektury 2019</h1></dt>
<dd><p class="text-left">KDE: centrum města Žatec <br> KDY: 5. října 2019 </p>
<p class="justify">POŘADATEL: Kruh, DA (Den architektury), FA (Film a architektura)</p>
<p class="justify">Druhé putování po žatecké architektuře s příznačným názvem Pěškobusem 1989 - 2019 jsme věnovali porevoluční tvorbě v našem městě. Trasu jsme zahájili před dostavbou Chmelařského muzea a pokračovali jsme dále do centra. Součástí procházky byla také exkurze do bývalé posádkové věznice a na vodárenskou věž Žateckého pivovaru. Tradiční duo Monika a Eva představilo významné novostavby a rekonstrukce posledních tří dekád, Lucie Radová z NPÚ vysvětlila základní principy památkové péče v městské památkové rezervaci. </p>

