<dt><h1>170. výročí založení divadla</h1></dt>
<dd><p class="text-left">KDE: Žatec <br> KDY: 22. dubna 2019</p>
<p class="text-left">POŘADATEL: Městské divadlo Žatec</p>

<a href="https://www.instagram.com/explore/tags/mdz_vystava2019/" target="_blank">#mdz_vystava2019</a>
<a href="https://www.instagram.com/explore/tags/adalvystavuje/" target="_blank">#adalvystavuje</a>
<a href="https://www.instagram.com/explore/tags/muzeumzatec/" target="_blank">#muzeumzatec</a>
<div class="mezera"></div>


<p class="justify">S nabídkou spolupráce nás na začátku roku 2019 oslovilo žatecké městské divadlo. V dubnu tomu bylo 170 let od otevření historické budovy divadla, kulturního stánku, na který se měšťané skládali a sami si jej za necelý rok postavili.
Ve druhém patře divadelního foyer jsme vytvořili a nainstalovali výstavu dokumentující stavebně - historický vývoj divadla. Tu si návštěvníci mohli poprvé prohlédnout o slavnostní divadelní večer, kdy se za bohatého programu na jevišti uskutečnil také křest dotisku knihy Jaroslava Venclíka. Komentovaná prohlídka poté proběhla v květnu o Muzejní noc. Výstavu je možné navštívit kdykoliv po vyžádání (druhé patro divadla není veřejnosti běžně zpřístupněno).
</p>
